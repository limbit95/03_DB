-- 19번 문제
SELECT DEPARTMENT_NAME "계열 학과명", ROUND(AVG(POINT), 1) "전공평점"
FROM TB_DEPARTMENT
JOIN TB_CLASS USING(DEPARTMENT_NO)
JOIN TB_GRADE USING(CLASS_NO)
WHERE CLASS_TYPE = '전공선택'
AND CATEGORY = '자연과학'
GROUP BY ROLLUP(DEPARTMENT_NAME, CLASS_NAME)
HAVING NVL(CLASS_NAME, ' ') = ' '
AND NVL(DEPARTMENT_NAME, NULL) IS NOT NULL
ORDER BY 1;