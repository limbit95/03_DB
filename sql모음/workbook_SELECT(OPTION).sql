FROM TB_GRADE
JOIN TB_CLASS USING(CLASS_NO)
WHERE DEPARTMENT_NO = '034'
AND CLASS_TYPE = '전공선택'
GROUP BY CLASS_NO, CLASS_NAME
ORDER BY 1;

